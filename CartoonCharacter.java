class CartoonCharacter {

   // Properties of the class...
   private String name;
   private String favouriteColour;
   private int    favouriteNumber;

   // Constructor of the class...
   public CartoonCharacter(String aName, String aColour, int aNumber) {
      name            = aName;
      favouriteColour = aColour;
      favouriteNumber = aNumber;
   }

   // Methods of the class...
   public void displayMe() {
      System.out.println("Hello, my name is " + name);
      System.out.println("my favourite colour is " + favouriteColour);
      System.out.println("and my favourite number is " + favouriteNumber);
   }
   
public static void main(String[] args) {
   CartoonCharacter f = new CartoonCharacter("fred flintstone","grey",10000);
   CartoonCharacter b = new CartoonCharacter("Barney Rubble","green",10000);
   CartoonCharacter w = new CartoonCharacter("Wilma Flintstone","pink",10000);
   
   f.displayMe();
   b.displayMe();
   w.displayMe();
   
   System.out.println(b.favouriteColour);
   
   System.out.println(Math.PI);
   }
   
}
